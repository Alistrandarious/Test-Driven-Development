class SimpleCalc:

    def add(self, num1: int, num2: int) -> int:
        add = num1 + num2
        return add

    def subtract(self, num1: int, num2: int) -> int:
        subtract = num1 - num2
        return subtract

    def multiply(self, num1:int, num2: int) -> int:
        multiply = num1 * num2
        return multiply

    def divide(self, num1: int, num2: int) -> int:
        divide = num1 // num2
        return divide
